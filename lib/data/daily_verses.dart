// 464 curated verses — same pool and hash algorithm as the PWA.
// Same day always picks the same verse; consecutive days jump around the
// full pool rather than walking the array in biblical book order.

class DailyVerse {
  const DailyVerse(
      {required this.book,
      required this.chapter,
      required this.verse,
      required this.ref});
  final String book;
  final int chapter, verse;
  final String ref;
}

int dailyVerseIndex() {
  final d = DateTime.now();
  final seed = d.year * 373 + d.month * 31 + d.day;
  final hash = ((seed * 1103515245 + 12345) & 0xFFFFFFFF);
  return hash % kDailyVerses.length;
}

DailyVerse todaysVerse() => kDailyVerses[dailyVerseIndex()];

